package encapsulation;

public class student 
{

	private String id;
	private double marks ;

	
	
	public String getId() 
	{
		return id;
	}
	
	public void setId(String id) 
	{
		this.id = id;
	}
	
	public double getMarks()
	{
		return marks;
		
	}
	
	public void setMarks(double marks) 
	{
		this.marks = marks;
	}

}
	
